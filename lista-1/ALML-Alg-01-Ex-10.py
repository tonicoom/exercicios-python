import math

a = int(input("Digite o primeiro número: "))
b = int(input("Digite o segundo número: "))
 
soma = a + b
sub = a - b
mult = a * b
div = a / b
rest = a % b 
log1 = math.log(10 ** a)
poten = a ** b


print("Soma: ", soma)
print("Subtração: ", sub)
print("Multiplicação: ", mult)
print("Divisão: ", div)
print("Resto: ", rest)
print("Log: ", log1)
print("Potência: ", poten)


